package com.detail;

public class FlightSearchDetail {
	private String FlightDate;
	private String AirlinesCode;
	private String AirlinesValue;
	private String FlightNumber;
	private String DepartureCode;
	private String DepartureValue;
	private String DestinationCode;
	private String DestinationValue;
	private String Gate;
	private String FlightTypeValue;

	public String getFlightDate() {
		return FlightDate;
	}

	public void setFlightDate(String flightDate) {
		FlightDate = flightDate;
	}

	public String getAirlinesCode() {
		return AirlinesCode;
	}

	public void setAirlinesCode(String airlinesCode) {
		AirlinesCode = airlinesCode;
	}

	public String getAirlinesValue() {
		return AirlinesValue;
	}

	public void setAirlinesValue(String airlinesValue) {
		AirlinesValue = airlinesValue;
	}

	public String getFlightNumber() {
		return FlightNumber;
	}

	public void setFlightNumber(String flightNumber) {
		FlightNumber = flightNumber;
	}

	public String getDepartureCode() {
		return DepartureCode;
	}

	public void setDepartureCode(String departureCode) {
		DepartureCode = departureCode;
	}

	public String getDepartureValue() {
		return DepartureValue;
	}

	public void setDepartureValue(String departureValue) {
		DepartureValue = departureValue;
	}

	public String getDestinationCode() {
		return DestinationCode;
	}

	public void setDestinationCode(String destinationCode) {
		DestinationCode = destinationCode;
	}

	public String getDestinationValue() {
		return DestinationValue;
	}

	public void setDestinationValue(String destinationValue) {
		DestinationValue = destinationValue;
	}

	public String getGate() {
		return Gate;
	}

	public void setGate(String gate) {
		Gate = gate;
	}

	public String getFlightType() {
		return FlightTypeValue;
	}

	public void setFlightType(String flightTypeValue) {
		FlightTypeValue = flightTypeValue;
	}

}
